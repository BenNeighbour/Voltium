def all_words(iterable, target):
    i = 0
    result = []
    while i < len(iterable):
        if iterable[i].lower() != target[0]:
            i += 1
            continue

        if iterable[i:i + len(target)].lower() == target:
            result.append(i)
            i += len(target)
        else:
            i += 1

    return result
    
