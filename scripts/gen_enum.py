def word_pass(line):
    for word in  sorted(words, reverse=True, key=len):
        indices = all_words(line, word)
        for index in indices:
            if index != 0 and line[index - 1] != "_":
                line = line[:index] + "_" + line[index:]

    return "".join(line)

